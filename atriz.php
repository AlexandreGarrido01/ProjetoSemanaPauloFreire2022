												O êxito do filme levou a Cinédia a chamá-la para um novo musical, Bonequinha de Seda. Esta, por algum motivo não elucidado, provavelmente viagem à Argentina, não aceitou o papel, que foi dado a Gilda de Abreu. Miranda logo passou a ser chamada de "A Pequena Notável" - por causa da estatura baixa, apelido dado nessa época, pelo radialista César Ladeira.</p>
									
