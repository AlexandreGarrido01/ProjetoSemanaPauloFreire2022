												Em 1926, ela apareceu como figurante no filme A Esposa do Solteiro, e quatro anos depois assinou contrato para Degraus da Vida, que não chegou a ser rodado. Miranda também apresentou um número musical em O Carnaval Cantado no Rio (1932), o primeiro documentário sonoro sobre o tema popular, e três músicas em A Voz do Carnaval (1933), que combinou imagens reais das celebrações do carnaval de rua no Rio com um enredo fictício que fornecia infinitos pretextos para números musicais.</p>
									
